import mongoose from "mongoose";

const adminSchema = new mongoose.Schema({
  fullname: {
    type: String,
    required: true,
  },
  username: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
    minlength: 6,
  },
});

const Admin = mongoose.model("Admin", adminSchema);

export default Admin;
